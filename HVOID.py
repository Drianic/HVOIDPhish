def barra_progreso(total):
    for i in range(total + 1):
        porcentaje = (i / total) * 100
        barra = '#' * int(porcentaje // 2) 
        sys.stdout.write(f'\r[{barra:50}] {porcentaje:.2f}%')
        sys.stdout.flush()
        time.sleep(0.1) 

